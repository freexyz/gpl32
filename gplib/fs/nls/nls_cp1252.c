// returns the cp1252 character from unicode
// return 0 is error
static unsigned int cp1252_uni2char(unsigned int uni)
{
	unsigned int c = 0;
	
	if (uni < 0x0080) 
	{
		return uni;
	}
	else if (uni >= 0x00a0 && uni < 0x0100)
		c = uni;
	else if (uni >= 0x0150 && uni < 0x0198)
		c = cp1252_page01[uni-0x0150];
	else if (uni >= 0x02c0 && uni < 0x02e0)
		c = cp1252_page02[uni-0x02c0];
	else if (uni >= 0x2010 && uni < 0x2040)
		c = cp1252_page20[uni-0x2010];
	else if (uni == 0x20ac)
		c = 0x80;
	else if (uni == 0x2122)
		c = 0x99;
	if (c != 0) 
	{
		return c;
	}
	if (uni < 0x100)
	{
		return uni;
	}
	return 0;
}

